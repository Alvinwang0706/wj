package cn.com.sinosoft.bean;

/**
 * @author wangcaiyun
 *
 */
public class UnderwritingOfflineInfo {

	private String productId;

    private String productName;

    private String name;

    private String mobile;

    private String email;

    private String situationDesc;

	public String getProductId() {
		return productId;
	}

	public void setProductId(String productId) {
		this.productId = productId;
	}

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getSituationDesc() {
		return situationDesc;
	}

	public void setSituationDesc(String situationDesc) {
		this.situationDesc = situationDesc;
	}
    
}
